    func testAlongExcel() {
        var companyBuilder = SimulationBuilder.builder().name("A Company").simulationName("Full simulation").numberOfSimulationSteps(12)
        companyBuilder = companyBuilder.increaseCreditLine(.of(2020, 1), Money.of("CHF", 13000000))
        companyBuilder = companyBuilder.buildStorage(.of(2020, 1), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 1), 1000)
        companyBuilder = companyBuilder.buildFactory(.of(2020, 1))
        companyBuilder = companyBuilder.enterMarket(.of(2020, 1))
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 3), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 4), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 5), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 6), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 7), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 8), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 9), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 10), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 11), 1000)
        companyBuilder = companyBuilder.buyRawMaterial(.of(2020, 12), 1000)
        let company = companyBuilder.build()
        var activeSimulationStep = company.simulation.getActiveSimulationStep()
        while let step = activeSimulationStep {
            let companySimulationStep = step.getCompanySimulationStep(for: company)
            companySimulationStep.finish()
            activeSimulationStep = company.simulation.getActiveSimulationStep()
        }
        let endOfYear = YearMonth.of(2020, 12).atEndOfMonth()
        XCTAssertEqual(Money.of("CHF", 5320000.00), company.accounting.getBankBalance(endOfYear))
        XCTAssertEqual(Money.of("CHF", -13000000.00), company.accounting.getLongTermDebt(endOfYear))
        XCTAssertEqual(Money.of("CHF", 2665673.44), company.accounting.getRealEstateBalance(endOfYear))
        XCTAssertEqual(Money.of("CHF", 8800000.00), company.accounting.getRevenues(endOfYear))
        XCTAssertEqual(Money.of("CHF", -4657926.56), company.accounting.getPnL(endOfYear))
        XCTAssertEqual(Money.of("CHF", -32605485.89), company.accounting.getCompanyValue(endOfYear))
        XCTAssertEqual(Money.of("CHF", 396000.00), company.accounting.getRawMaterialBalance(endOfYear))
        XCTAssertEqual(Money.of("CHF", -39600.00), company.accounting.getProductBalance(endOfYear))
    }

