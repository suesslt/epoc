    func testTenYearsInYearSteps() {
        let companyBuilder = SimulationBuilder.builder().name("A Ten Year Company").simulationName("Full simulation for ten years").numberOfSimulationSteps(120).passiveSteps(12)
        companyBuilder.increaseCreditLine(.of(2020, 3), Money.of("CHF", 100000000))
        companyBuilder.buildStorage(.of(2020, 6), 10000)
        companyBuilder.buyRawMaterial(.of(2020, 9), 10000)
        companyBuilder.buildFactory(.of(2020, 12))
        companyBuilder.enterMarket(.of(2021, 3))
        companyBuilder.increaseCreditLine(.of(2021, 6), Money.of("CHF", 100000000))
        companyBuilder.buyRawMaterial(.of(2022, 8), 10000)
        let company = companyBuilder.build()
        var activeSimulationStep = company.simulation.getActiveSimulationStep()
        var activeStepCounter = 0
        while let step = activeSimulationStep {
            activeStepCounter += 1
            let companySimulationStep = step.getCompanySimulationStep(for: company)
            companySimulationStep.finish()
            activeSimulationStep = company.simulation.getActiveSimulationStep()
        }
        let endDate = YearMonth.of(2030, 1).atEndOfMonth()
        XCTAssertEqual(Money.of("CHF", 16768000), company.accounting.getRevenues(endDate))
        XCTAssertEqual(10, activeStepCounter)
        XCTAssertEqual(120, company.companySimulationSteps.count)
        XCTAssertEqual(120, company.simulation.simulationSteps.count)
    }

